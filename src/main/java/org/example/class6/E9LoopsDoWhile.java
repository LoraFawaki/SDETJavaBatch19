package org.example.class6;

public class E9LoopsDoWhile {
    public static void main(String[] args) {

        // while loop --> checks the condition first then execute the code inside the body
        // do while loop --> executes the code first and then check the condition (code will execute at least once,
        //before checking the condition true or false.

        double num=1;
        do{
            System.out.println("Java is fun");
            num++;

        }while (num<5);


    }
}
