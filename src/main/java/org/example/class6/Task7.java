package org.example.class6;

public class Task7 {
    public static void main(String[] args) {

        // print even numbers from 20 to 100

        for (int i=20; i<=100; i=i+2){
            System.out.print(i + " ");
        }
        System.out.println();

        //print odd numbers from 100 to 1
        for (int i=99; i>=0; i=i-2){
            System.out.print(i+" ");
        }
    }
}
