package org.example.class3;

public class E3Short {
    public static void main(String[] args) {
        int number=100;
        //number=number*10; = number*=10;

        number*=10;
        //will multiply 10 to the value of the variant "number"
        System.out.println(number);

        int number1=100;
        //number=number/10; = number/=10;

        number1/=10;
        //will divide 10 to the value of the variant "number"
        System.out.println(number1);

        int number2=100;
        //number=number%9; = number%=9;

        number2 %=9;
        //will divide 10 to the value of the variant "number"
        System.out.println(number2);

    }
}
